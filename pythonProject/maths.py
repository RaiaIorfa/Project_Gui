import math

pi = 3.142
class maths:
    def areaofsquare(self,length):
        return (length**2)
    def areaoftriangle(self,base,height):
        return (0.5*base*height)
    def volumeofcone(self,radius,height):
        return (0.3*math.pi*radius*radius*height)
    def areaofrectangle(self,length,breath):
        return (length*breath)
    def circumferenceofcircle(self,radius):
        return (2*math.pi*radius*radius)